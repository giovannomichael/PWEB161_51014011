<h1><center>FUTSAL</h1></center>
<hr>

<h2>
Futsal adalah permainan bola yang dimainkan oleh dua tim, yang masing-masing beranggotakan lima orang.<br>Tujuannya adalah memasukkan bola ke gawang lawan, dengan memanipulasi bola dengan kaki. Selain lima pemain utama, setiap regu juga diizinkan memiliki pemain cadangan.<br>Tidak seperti permainan sepak bola dalam ruangan lainnya, lapangan futsal dibatasi garis, bukan net atau papan.
Futsal turut juga dikenali dengan berbagai nama lain.<br>Istilah "futsal" adalah istilah internasionalnya, berasal dari kata Spanyol atau Portugis, futbol dan sala.
</h2>
